# ================================================================
# MAIN ANALYSIS ENGINE
# ================================================================

class ClinicalAnalysisEngine:
    """Orchestrates the full clinical analysis pipeline."""

    def __init__(self, mode: str = "local"):
        """
        Args:
            mode: "local" (offline template-based), "anthropic", or "openai"
        """
        self.mode = mode
        self.report_analyzer = ReportAnalyzer()
        self.local_engine = LocalAnalysisEngine()

    def analyze_report(self, pdf_path: str) -> ClinicalReport:
        """Full pipeline: PDF → Extract → RAG → LLM Analysis → Clinical Report."""

        # Step 1: Extract + RAG enrich
        enriched = self.report_analyzer.analyze(pdf_path)

        # Step 2: Generate biomarker interpretations
        other_abnormals = [
            f"{ba.test_name}: {ba.value} {ba.unit} ({ba.flag})"
            for ba in enriched.biomarker_analyses
        ]

        interpretations = []
        for ba in enriched.biomarker_analyses:
            if self.mode == "local":
                interp = self.local_engine.interpret_biomarker(
                    ba, other_abnormals, enriched.patient_sex
                )
            else:
                interp = self._llm_interpret_biomarker(ba, other_abnormals, enriched)

            interpretations.append(interp)

        # Step 3: Pattern analysis
        if self.mode == "local":
            patterns = self.local_engine.analyze_patterns(enriched)
        else:
            patterns = self._llm_analyze_patterns(enriched)

        # Step 4: Executive summary
        if self.mode == "local":
            summary, lifestyle, followup = self.local_engine.generate_summary(
                enriched, interpretations, patterns
            )
        else:
            summary, lifestyle, followup = self._llm_generate_summary(
                enriched, interpretations, patterns
            )

        return ClinicalReport(
            patient_name=enriched.patient_name,
            patient_age=enriched.patient_age,
            patient_sex=enriched.patient_sex,
            lab_name=enriched.lab_name,
            report_date="",
            total_tests=enriched.total_tests,
            abnormal_count=enriched.abnormal_count,
            critical_count=enriched.critical_count,
            executive_summary=summary,
            biomarker_interpretations=interpretations,
            pattern_interpretations=patterns,
            lifestyle_recommendations=lifestyle,
            follow_up_tests=followup,
        )

    def _llm_interpret_biomarker(self, ba: BiomarkerAnalysis,
                                  other_abnormals: list[str],
                                  enriched: EnrichedReport) -> BiomarkerInterpretation:
        """Use LLM API for biomarker interpretation."""
        rag_context = "\n".join(
            f"- {r['title']}: {r['content'][:300]}\n  Source: {r['source']}"
            for r in ba.rag_interpretations[:2]
        ) or "No specific RAG context available."

        prompt = BIOMARKER_PROMPT_TEMPLATE.format(
            name=enriched.patient_name, age=enriched.patient_age, sex=enriched.patient_sex,
            test_name=ba.test_name, canonical_name=ba.canonical_name,
            value=ba.value, unit=ba.unit, flag=ba.flag,
            deviation_pct=ba.deviation_pct, band=ba.interpretation_band or "N/A",
            rag_context=rag_context,
            other_abnormals="\n".join(f"  - {o}" for o in other_abnormals[:10]),
        )

        try:
            call_fn = _call_anthropic if self.mode == "anthropic" else _call_openai
            response = call_fn(SYSTEM_PROMPT, prompt)
            data = _parse_json_response(response)

            sources = [r.get("source", "") for r in ba.rag_interpretations[:2]]

            return BiomarkerInterpretation(
                test_name=ba.test_name, canonical_name=ba.canonical_name,
                value=ba.value, unit=ba.unit, flag=ba.flag,
                deviation_pct=ba.deviation_pct, interpretation_band=ba.interpretation_band,
                clinical_significance=data.get("clinical_significance", ""),
                possible_causes=data.get("possible_causes", []),
                recommended_actions=data.get("recommended_actions", []),
                urgency=data.get("urgency", "routine"),
                sources=sources,
            )
        except Exception:
            # Fallback to local
            return self.local_engine.interpret_biomarker(ba, other_abnormals, enriched.patient_sex)

    def _llm_analyze_patterns(self, enriched: EnrichedReport) -> list[PatternInterpretation]:
        """LLM-based pattern analysis (fallback to local)."""
        return self.local_engine.analyze_patterns(enriched)

    def _llm_generate_summary(self, enriched, interpretations, patterns):
        """LLM-based summary (fallback to local)."""
        return self.local_engine.generate_summary(enriched, interpretations, patterns)


