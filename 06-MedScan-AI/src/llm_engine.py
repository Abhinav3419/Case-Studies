def format_clinical_report(report: ClinicalReport) -> str:
    """Format the clinical report for display."""
    lines = []
    lines.append("=" * 72)
    lines.append("  MEDSCAN AI — CLINICAL ANALYSIS REPORT")
    lines.append("=" * 72)

    lines.append(f"\n  Patient: {report.patient_name}")
    lines.append(f"  Age/Sex: {report.patient_age} / {report.patient_sex}")
    lines.append(f"  Lab: {report.lab_name}")
    lines.append(f"  Panel: {report.total_tests} tests | {report.abnormal_count} abnormal | {report.critical_count} critical")

    lines.append(f"\n{'─' * 72}")
    lines.append(f"  EXECUTIVE SUMMARY")
    lines.append(f"{'─' * 72}")
    lines.append(f"  {report.executive_summary}")

    lines.append(f"\n{'─' * 72}")
    lines.append(f"  BIOMARKER INTERPRETATIONS ({len(report.biomarker_interpretations)})")
    lines.append(f"{'─' * 72}")

    for i, bi in enumerate(report.biomarker_interpretations, 1):
        icon = "🔺" if "HIGH" in bi.flag else "🔻"
        urg_icon = {"routine": "🟢", "soon": "🟡", "urgent": "🟠", "emergency": "🔴"}.get(bi.urgency, "⚪")

        lines.append(f"\n  {icon} {i}. {bi.test_name}: {bi.value} {bi.unit} [{bi.flag}] {urg_icon} {bi.urgency.upper()}")
        lines.append(f"     {bi.clinical_significance}")
        lines.append(f"     Possible causes: {' | '.join(bi.possible_causes[:3])}")
        lines.append(f"     Actions: {' | '.join(bi.recommended_actions[:2])}")
        if bi.sources:
            lines.append(f"     Ref: {bi.sources[0]}")

    if report.pattern_interpretations:
        lines.append(f"\n{'─' * 72}")
        lines.append(f"  COMBINED PATTERN ANALYSIS ({len(report.pattern_interpretations)})")
        lines.append(f"{'─' * 72}")

        for p in report.pattern_interpretations:
            lines.append(f"\n  🔗 {p.pattern_name}")
            lines.append(f"     Reasoning: {p.clinical_reasoning[:200]}...")
            lines.append(f"     Suggests: {p.diagnosis_suggestion}")
            lines.append(f"     Workup: {' | '.join(p.recommended_workup[:3])}")
            if p.sources:
                lines.append(f"     Ref: {p.sources[0]}")

    lines.append(f"\n{'─' * 72}")
    lines.append(f"  LIFESTYLE RECOMMENDATIONS")
    lines.append(f"{'─' * 72}")
    for r in report.lifestyle_recommendations:
        lines.append(f"  • {r}")

    lines.append(f"\n{'─' * 72}")
    lines.append(f"  RECOMMENDED FOLLOW-UP TESTS")
    lines.append(f"{'─' * 72}")
    for t in report.follow_up_tests:
        lines.append(f"  • {t}")

    lines.append(f"\n{'─' * 72}")
    lines.append(f"  ⚕️  DISCLAIMER")
    lines.append(f"{'─' * 72}")
    lines.append(f"  {report.disclaimer}")

    lines.append(f"\n{'=' * 72}")
    return "\n".join(lines)


def report_to_json(report: ClinicalReport) -> str:
    """Serialize to JSON."""
    return json.dumps(asdict(report), indent=2, ensure_ascii=False)


